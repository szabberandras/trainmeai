// Helper functions for accessing Half Marathon plans
export function getHalfMarathonPlansByWeek(week: number) {
  return HALF_MARATHON_PLANS.filter(plan => plan.week === week);
}

export function getHalfMarathonPlanByWeekAndDay(week: number, day: number) {
  return HALF_MARATHON_PLANS.find(plan => plan.week === week && plan.day === day);
}

export function getAllHalfMarathonWeeks() {
  const weeks = [...new Set(HALF_MARATHON_PLANS.map(plan => plan.week))];
  return weeks.sort((a, b) => a - b);
}

export function getHalfMarathonPlanSummary() {
  const weeks = getAllHalfMarathonWeeks();
  return {
    total_weeks: weeks.length,
    total_sessions: HALF_MARATHON_PLANS.length,
    program_name: "Half Marathon - 21km Training Plan",
    weeks: weeks
  };
} 