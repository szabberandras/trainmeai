export class ExerciseVariationsService {
  /**
   * Get all variations for a specific exercise
   */
  static getVariationsForExercise(exerciseId: string): ExerciseVariation[] {
    return EXERCISE_VARIATIONS[exerciseId] || [];
  }

  /**
   * Get variations by category (progression, regression, etc.)
   */
  static getVariationsByCategory(
    exerciseId: string, 
    category: ExerciseVariation['category']
  ): ExerciseVariation[] {
    const variations = this.getVariationsForExercise(exerciseId);
    return variations.filter(variation => variation.category === category);
  }

  /**
   * Get progressions (harder variations) for an exercise
   */
  static getProgressions(exerciseId: string): ExerciseVariation[] {
    return this.getVariationsByCategory(exerciseId, 'progression');
  }

  /**
   * Get regressions (easier variations) for an exercise
   */
  static getRegressions(exerciseId: string): ExerciseVariation[] {
    return this.getVariationsByCategory(exerciseId, 'regression');
  }

  /**
   * Get equipment alternatives for an exercise
   */
  static getEquipmentVariations(exerciseId: string): ExerciseVariation[] {
    return this.getVariationsByCategory(exerciseId, 'equipment');
  }

  /**
   * Get variations based on available equipment
   */
  static getVariationsForEquipment(
    exerciseId: string, 
    availableEquipment: string[]
  ): ExerciseVariation[] {
    const variations = this.getVariationsForExercise(exerciseId);
    return variations.filter(variation => 
      variation.equipment.every(eq => 
        availableEquipment.includes(eq) || eq === 'bodyweight' || eq === ''
      )
    );
  }

  /**
   * Get variations based on difficulty level
   */
  static getVariationsForDifficulty(
    exerciseId: string, 
    targetDifficulty: number
  ): ExerciseVariation[] {
    const variations = this.getVariationsForExercise(exerciseId);
    return variations.filter(variation => variation.difficulty === targetDifficulty);
  }

  /**
   * Find the best variation based on user constraints
   */
  static findBestVariation(
    exerciseId: string,
    constraints: {
      availableEquipment?: string[];
      difficultyPreference?: 'easier' | 'same' | 'harder';
      targetMuscleGroups?: string[];
      avoidCategories?: ExerciseVariation['category'][];
    }
  ): ExerciseVariation | null {
    let variations = this.getVariationsForExercise(exerciseId);

    // Filter by equipment
    if (constraints.availableEquipment) {
      variations = variations.filter(variation => 
        variation.equipment.every(eq => 
          constraints.availableEquipment!.includes(eq) || eq === 'bodyweight' || eq === ''
        )
      );
    }

    // Filter by difficulty preference
    if (constraints.difficultyPreference) {
      const baseExercise = EXERCISE_DATABASE[exerciseId];
      const baseDifficulty = baseExercise?.difficulty || 3;

      variations = variations.filter(variation => {
        switch (constraints.difficultyPreference) {
          case 'easier':
            return variation.difficulty < baseDifficulty;
          case 'harder':
            return variation.difficulty > baseDifficulty;
          case 'same':
            return variation.difficulty === baseDifficulty;
          default:
            return true;
        }
      });
    }

    // Filter by target muscle groups
    if (constraints.targetMuscleGroups) {
      variations = variations.filter(variation =>
        variation.muscleGroups.some(mg => 
          constraints.targetMuscleGroups!.includes(mg)
        )
      );
    }

    // Avoid certain categories
    if (constraints.avoidCategories) {
      variations = variations.filter(variation =>
        !constraints.avoidCategories!.includes(variation.category)
      );
    }

    // Return the first suitable variation, or null if none found
    return variations.length > 0 ? variations[0] : null;
  }

  /**
   * Get all missing exercises that should be added to the database
   */
  static getMissingExercises(): string[] {
    return MISSING_EXERCISES;
  }

  /**
   * Check if an exercise has variations available
   */
  static hasVariations(exerciseId: string): boolean {
    return exerciseId in EXERCISE_VARIATIONS;
  }

  /**
   * Get variation statistics
   */
  static getVariationStats(): {
    totalExercisesWithVariations: number;
    totalVariations: number;
    variationsByCategory: Record<ExerciseVariation['category'], number>;
  } {
    const totalExercisesWithVariations = Object.keys(EXERCISE_VARIATIONS).length;
    const allVariations = Object.values(EXERCISE_VARIATIONS).flat();
    const totalVariations = allVariations.length;

    const variationsByCategory = allVariations.reduce((acc, variation) => {
      acc[variation.category] = (acc[variation.category] || 0) + 1;
      return acc;
    }, {} as Record<ExerciseVariation['category'], number>);

    return {
      totalExercisesWithVariations,
      totalVariations,
      variationsByCategory
    };
  }

  /**
   * Generate AI-friendly exercise variation suggestions
   */
  static generateVariationSuggestions(
    exerciseId: string,
    userContext: {
      fitnessLevel?: 'beginner' | 'intermediate' | 'advanced';
      availableEquipment?: string[];
      injuries?: string[];
      goals?: string[];
    }
  ): {
    primary: ExerciseVariation | null;
    alternatives: ExerciseVariation[];
    reasoning: string;
  } {
    const variations = this.getVariationsForExercise(exerciseId);
    const baseExercise = EXERCISE_DATABASE[exerciseId];

    if (variations.length === 0) {
      return {
        primary: null,
        alternatives: [],
        reasoning: `No variations available for ${baseExercise?.name || exerciseId}`
      };
    }

    // Determine difficulty preference based on fitness level
    let difficultyPreference: 'easier' | 'same' | 'harder' = 'same';
    if (userContext.fitnessLevel === 'beginner') {
      difficultyPreference = 'easier';
    } else if (userContext.fitnessLevel === 'advanced') {
      difficultyPreference = 'harder';
    }

    // Find best primary variation
    const primary = this.findBestVariation(exerciseId, {
      availableEquipment: userContext.availableEquipment,
      difficultyPreference
    });

    // Get alternative variations
    const alternatives = variations
      .filter(v => v.id !== primary?.id)
      .slice(0, 3);

    // Generate reasoning
    let reasoning = `For ${baseExercise?.name || exerciseId}: `;
    if (primary) {
      reasoning += `Recommended ${primary.name} because it ${primary.description.toLowerCase()}`;
      if (userContext.fitnessLevel) {
        reasoning += ` and matches your ${userContext.fitnessLevel} fitness level`;
      }
      if (userContext.availableEquipment) {
        reasoning += ` with your available equipment`;
      }
    } else {
      reasoning += `No suitable variations found for your current constraints`;
    }

    return {
      primary,
      alternatives,
      reasoning
    };
  }
}